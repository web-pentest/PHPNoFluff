/*
if, else, elseif — позволяют выполнять разный код в зависимости от условий.

ОПЕРАТОРЫ СРАВНЕНИЯ (запомни их!):
  ==    равно (не строго, "1" == 1 — true)
  ===   строго равно (и значение, и тип одинаковы)
