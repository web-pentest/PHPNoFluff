/*
Циклы нужны, когда надо сделать одно и то же много раз.
Например: вывести 100 товаров, отправить уведомления всем пользователям.

ЧЕТЫРЕ ВИДА ЦИКЛОВ:
  - while    — пока условие true, делай
  - do-while — сделай раз, потом проверяй
  - for      — когда знаешь, сколько раз повторить
  - foreach  — для массивов (используй чаще всего)
*/

// WHILE (пока условие true — делай):
$i = 1;
while ($i <= 5) {
    echo "Итерация $i\n";
    $i++;  // НЕ ЗАБУДЬ! Иначе бесконечный цикл и пиздец.
}

// DO-WHILE (сначала сделай, потом проверь):
$i = 1;
do {
    echo "Выполнится хотя бы раз: $i\n";
    $i++;
} while ($i <= 0);  // условие false, но тело выполнилось 1 раз

// FOR (счётчик, когда знаешь количество итераций):
for ($i = 1; $i <= 10; $i++) {
    echo "Квадрат $i = " . ($i * $i) . "\n";
}

// FOREACH (для массивов — используй чаще всего, бля):
$users = ["Анна", "Борис", "Виктор"];
foreach ($users as $user) {
    echo "Привет, $user!\n";
}

// FOREACH с ключами (для ассоциативных массивов):
$goods = [
    "яблоко" => 100,
    "банан" => 80,
    "апельсин" => 120
];
foreach ($goods as $product => $price) {
    echo "$product стоит $price руб.\n";
}

// BREAK — выйти из цикла досрочно:
for ($i = 1; $i <= 10; $i++) {
    if ($i === 5) {
        break;  // остановиться на 5
    }
    echo $i;
}  // выведет: 1234

// CONTINUE — пропустить итерацию:
for ($i = 1; $i <= 10; $i++) {
    if ($i % 2 === 0) {
        continue;  // пропустить чётные
    }
    echo $i;
}  // выведет: 13579

