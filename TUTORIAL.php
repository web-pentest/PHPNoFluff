/*
Циклы нужны, когда нужно повторить одно действие много раз.
Например: вывести 100 товаров из массива, отправить уведомления всем пользователям.

ЧЕТЫРЕ ВИДА ЦИКЛОВ:
  - while  — проверяет условие ДО выполнения
  - do-while — проверяет условие ПОСЛЕ выполнения (выполнится хотя бы раз)
